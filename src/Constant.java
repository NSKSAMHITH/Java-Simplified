public class Constant {
    public static void main(String[] args){
        int age = 28;

        age = 18;
        System.out.println(age);

        age = 22;
        System.out.println(age);

        age = 25;
        System.out.println(age);

        //use Final keyword to ensure the value remains constant and unchanged.
        final float PI = 3.14F;
        System.out.println(PI);
    }
}
